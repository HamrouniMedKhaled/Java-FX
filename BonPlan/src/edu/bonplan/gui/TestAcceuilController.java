    public Pane createPane(Badge badge){
        Pane p = new Pane();
        Button b = new Button();
        b.setText(badge.getName());
        b.setPrefWidth(350);
        b.setPrefHeight(75);
        b.setLayoutX(0);
        b.setOnAction((ActionEvent e) -> {
             try {
                Parent p5=FXMLLoader.load(getClass().getResource("/edu/bonplan/gui/BagdeDetail.fxml"));
                Stage stage=new Stage();
                Scene scene = new Scene(p5);
            
                stage.setTitle("DÃ©tail du Badge");
                stage.setScene(scene);
                stage.show();
            } catch (IOException ex) {
                Logger.getLogger(AjouterDealController.class.getName()).log(Level.SEVERE, null, ex);
        }
             
             
            
        });
        
        badgeList.setItems(badges);
        
        p.getChildren().add(b);
    
    return p;
    }

    
    
