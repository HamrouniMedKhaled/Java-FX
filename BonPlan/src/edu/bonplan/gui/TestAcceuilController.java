    @Override
    public void initialize(URL url, ResourceBundle rb) {
        BadList = cs.findUlockedFor(Vars.current_user.getId());
        BadList.forEach(b->{
            Pane p = new Pane();
            p = createPane(b);
            badges.add(p);
            Vars.current_badge= b;
            
        });
        badgeList.setItems(badges);
       
    }    

